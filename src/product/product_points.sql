CREATE TABLE product_points
(
	product_points_id INTEGER GENERATED ALWAYS AS IDENTITY,
  product_id INTEGER NOT NULL,
  points_outer REAL NOT NULL DEFAULT 0.0,
  points_inner REAL NOT NULL DEFAULT 0.0,
  points_discount REAL NOT NULL DEFAULT 0.0,
  
	CONSTRAINT pk_product_points_product_points_id PRIMARY KEY (product_points_id),
  CONSTRAINT unq_product_points_product_id UNIQUE (product_id),
  CONSTRAINT chk_product_points_points_outer CHECK (points_outer >= 0),
  CONSTRAINT chk_product_points_points_inner CHECK (points_inner >= 0),
  CONSTRAINT chk_product_points_points_discount CHECK (points_discount >= 0),
  CONSTRAINT fk_product_points_product_id_product_product_id FOREIGN KEY (product_id) REFERENCES product(product_id)
);


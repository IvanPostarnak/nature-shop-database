DROP TABLE IF EXISTS product_size;

CREATE TABLE product_size
(
	product_size_id INTEGER GENERATED ALWAYS AS IDENTITY,
  product_id INTEGER NOT NULL,
  weight_g REAL NOT NULL,
  length_cm REAL NOT NULL,
  width_cm REAL NOT NULL,
  height_cm REAL NOT NULL,
  packing_material_id INTEGER NOT NULL,
  
	CONSTRAINT pk_product_size_product_size_id PRIMARY KEY (product_size_id),
  CONSTRAINT unq_product_size_product_id UNIQUE (product_id),
  CONSTRAINT chk_product_size_weight_g CHECK (weight_g > 0),
  CONSTRAINT chk_product_size_length_cm CHECK (length_cm > 0),
  CONSTRAINT chk_product_size_width_cm CHECK (width_cm > 0),
  CONSTRAINT chk_product_size_height_cm CHECK (height_cm > 0),
  CONSTRAINT fk_product_size_product_id_product_product_id FOREIGN KEY (product_id) REFERENCES product(product_id),
  CONSTRAINT fk_product_size_packing_material_id_packing_material_packing_material_id FOREIGN KEY (packing_material_id) REFERENCES packing_material(packing_material_id)
);

